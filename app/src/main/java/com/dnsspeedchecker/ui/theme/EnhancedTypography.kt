// Typography Extensions
fun TextStyle.withWeight(weight: FontWeight): TextStyle {
    return this.copy(fontWeight = weight)
}

fun TextStyle.withSize(size: Int): TextStyle {
    return this.copy(fontSize = size.sp)
}

fun TextStyle.withLetterSpacing(spacing: Float): TextStyle {
    return this.copy(letterSpacing = spacing.sp)
}

fun TextStyle.withLineHeight(height: Int): TextStyle {
    return this.copy(lineHeight = height.sp)
}

fun TextStyle.withAlpha(alpha: Float): TextStyle {
    return this.copy(color = this.color?.copy(alpha = alpha))
}
